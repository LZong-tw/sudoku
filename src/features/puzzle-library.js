/**
 * Get all puzzles for a specific difficulty
 * @param {string} difficulty - Difficulty level ('easy'|'medium'|'hard')
 * @returns {Array<PuzzleData>} Array of puzzles for the difficulty
 */
export function getPuzzlesByDifficulty(difficulty) {
  switch (difficulty.toLowerCase()) {
    case 'easy':
      return EASY_PUZZLES;
    case 'medium':
      return MEDIUM_PUZZLES;
    case 'hard':
      return HARD_PUZZLES;
    default:
      console.warn(`Unknown difficulty: ${difficulty}, returning easy puzzles`);
      return EASY_PUZZLES;
  }
}

/**
 * Get total number of puzzles
 * @returns {number} Total puzzle count
 */
export function getTotalPuzzleCount() {
  return EASY_PUZZLES.length + MEDIUM_PUZZLES.length + HARD_PUZZLES.length;
}

/**
 * Get puzzle by ID
 * @param {string} puzzleId - Puzzle identifier
 * @returns {PuzzleData|null} Puzzle data or null if not found
 */
export function getPuzzleById(puzzleId) {
  const allPuzzles = [...EASY_PUZZLES, ...MEDIUM_PUZZLES, ...HARD_PUZZLES];
  return allPuzzles.find(p => p.id === puzzleId) || null;
}
